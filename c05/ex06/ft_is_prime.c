int	ft_is_prime(int nb)
{
	long long	i;

	i = 2;
	if (nb <= 1)
		return (0);
	while (i * i <= nb)
	{
		if (i >= 46341)
			return (0);
		if (nb % i == 0)
			return (0);
		i++;
	}
	return (1);
}
